#!/bin/bash
# verify_key_cert.sh — проверка соответствия ключа и сертификата

KEY=${1}    # первый аргумент — путь к приватному ключу
CERT=${2}   # второй аргумент — путь к сертификату

# Если аргументы не переданы — показываем подсказку и выходим
if [[ -z "$KEY" || -z "$CERT" ]]; then
    echo "Использование: $0 <key.pem> <cert.pem>"
    exit 1
fi

echo "=== Проверка соответствия ключа и сертификата ==="

# Берём модуль из приватного ключа и хэшируем md5
KEY_MOD=$(openssl rsa -noout -modulus -in "$KEY" | md5sum)

# Берём модуль из сертификата и хэшируем md5
# Если ключ и сертификат — пара, модули будут одинаковые
CERT_MOD=$(openssl x509 -noout -modulus -in "$CERT" | md5sum)

echo "Ключ:        $KEY_MOD"
echo "Сертификат:  $CERT_MOD"

# Сравниваем хэши — если совпали, всё ок
if [[ "$KEY_MOD" == "$CERT_MOD" ]]; then
    echo "[OK] Ключ и сертификат совпадают"
else
    echo "[FAIL] Ключ и сертификат НЕ совпадают!"
    exit 1
fi